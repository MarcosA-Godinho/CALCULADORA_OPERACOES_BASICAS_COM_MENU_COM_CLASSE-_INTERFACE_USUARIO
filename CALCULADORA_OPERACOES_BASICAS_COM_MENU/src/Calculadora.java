public class Calculadora {

    public double somaNumeros(double numeroA, double numeroB) {
        double resultado = numeroA + numeroB;

        return resultado;
    }
    public double subtracaoNumeros(double numeroA, double numeroB){
        double resultado = numeroA - numeroB;

        return resultado;
    }
    public double multiplicacaoNumeros(double numeroA, double numeroB){
        double resultado = numeroA * numeroB;
        return  resultado;
    }
    public double divisaoNumeros(double numeroA, double numeroB){
        double resultado = numeroA / numeroB;
        return resultado;
    }
}
